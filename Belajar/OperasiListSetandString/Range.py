# Range dengan 1 parameter n:
# membuat deret bilangan yang di mulai dari 0
# sebanyak n bilangan
for i in range(5):
    print(i)
print(
    '==='
)
# range dengan 2 parameter n, p:
# membuat deret yang di mulai dengan yang dimulai dengan n ,
# hingga  sebelum p ( bilangan p tidak ikut)
# sering di sebut sebgai iklusif n ( di mulai dari deret n)
# dan ekslusif p(deret tidak menyertakan p )
for i in range(1, 11):
    print(i)
print(
    '==='
)
# range dengan 3 parameter n, p , q:
# membuat deret bilangan yang di mulai dari n dan berhenti di p ( ilangan p tidak ikut)
# dan setiap element nymemiliki selisih dengn q

for i in range(0, 20, 5):
    print(i)
