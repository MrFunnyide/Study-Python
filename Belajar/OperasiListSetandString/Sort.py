# Sort() = mengurutkan angka dan huruf
angka = [17, 45, 76, 98, 100, 45, 99, 3]
angka.sort()
print(angka)

kendaraan = ['Motor', 'Honda', 'Sepeda', 'Angka', 'Bumerang']
kendaraan.sort()  # bisa juga menambahkan reverse = true untuk membalikkan
print(kendaraan)

