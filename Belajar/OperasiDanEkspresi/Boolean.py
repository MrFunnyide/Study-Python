# Not (Boolean Not)
x = True  # apabila false maka not x = true
print(not x)

# and (Boolean And)
a = True; b = False  # jika hanya satu yang true = false, jika dua duanya true = true
print(a and b)

# or (Boolean Or)
c = False; d = True  # menghasilkan true apabila ada salah satu yan true
print(c or d)
