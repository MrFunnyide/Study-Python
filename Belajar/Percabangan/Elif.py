Bilangan = int(input('masukkan nilai : '))
if Bilangan > 0:
    print('Bilangan {} termasuk kedalam bilangan positif'.format(Bilangan))
elif Bilangan < 0:
    print('Bilangan {} termasuk kedalam bilangan negatif'.format(Bilangan))
else:
    print('Bilangan {} termasuk bilangan nol'.format(Bilangan))
